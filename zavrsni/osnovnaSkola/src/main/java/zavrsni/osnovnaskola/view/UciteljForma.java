private void ucitaj() {

        DefaultListModel<Ucitelj> m = new DefaultListModel<>();

        m.addAll(obrada.getPodaci());

        lstUcitelji.setModel(m);

    }

    private void postaviVrijednostiNaEntitet() {
        var entitet=obrada.getEntitet();
        
        entitet.setIme(txtIme.getText());
        entitet.setPrezime(txtPrezime.getText());

        try {
            entitet.setOib(txtOib.getText());
        } catch (Exception e) {
            entitet.setOib("");
        }
        try {
            entitet.setIban(txtIban.getText());
        } catch (Exception e) {
            entitet.setIban("");
        }
        try {
            entitet.setEmail(txtEmail.getText());
        } catch (Exception e) {
            entitet.setEmail("");
        }


    }

    private void pocisti() {
        txtIme.setText("");
        txtPrezime.setText("");
        txtOib.setText("");
        txtIban.setText("");
        txtEmail.setText("");

    }
