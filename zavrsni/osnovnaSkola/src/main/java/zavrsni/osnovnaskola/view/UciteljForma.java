    private void lstUciteljiValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstUciteljiValueChanged
        if (evt.getValueIsAdjusting()) {
            return;
        }
       
        if (lstUcitelji.getSelectedValue() == null) {
            return;
        }
        
         obrada.setEntitet(lstUcitelji.getSelectedValue());


        // ovo se može zamijeniti tkz. Binding
        txtIme.setText(obrada.getEntitet().getIme());
        txtPrezime.setText(obrada.getEntitet().getPrezime());
        
        try {
            txtOib.setText(obrada.getEntitet().getOib());
        } catch (Exception e) {
            txtOib.setText("");
        }
        try {
            txtIban.setText(obrada.getEntitet().getIban());
        } catch (Exception e) {
            txtIban.setText("");
        }
        try {
            txtEmail.setText(obrada.getEntitet().getEmail());
        } catch (Exception e) {
            txtEmail.setText("");
        }

    }//GEN-LAST:event_lstUciteljiValueChanged

    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        obrada.setEntitet(new Ucitelj());
        postaviVrijednostiNaEntitet();

        try {
            obrada.create();
            pocisti();
            ucitaj(); //nije optimizirano. Bolje bi bilo samo taj novi dodati u listu
        } catch (EdunovaException ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getPoruka());
        }
    }//GEN-LAST:event_btnDodajActionPerformed

    private void btnPromjeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPromjeniActionPerformed
        if (obrada.getEntitet()==null || 
                obrada.getEntitet().getId() == null) {
            JOptionPane.showMessageDialog(rootPane, "Prvo odaberite stavku");
            return;
        }
        postaviVrijednostiNaEntitet();

        try {
            obrada.update();
            pocisti();
            ucitaj();
        } catch (EdunovaException e) {
            JOptionPane.showMessageDialog(rootPane, e.getPoruka());
        }
    }//GEN-LAST:event_btnPromjeniActionPerformed

    private void btnIzbrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzbrisiActionPerformed
        if (obrada.getEntitet()==null || 
                obrada.getEntitet().getId() == null) {
            JOptionPane.showMessageDialog(rootPane, "Prvo odaberite stavku");
            return;
        }

        try {
            obrada.delete();
            pocisti();
            ucitaj();
        } catch (EdunovaException e) {
            JOptionPane.showMessageDialog(rootPane, e.getPoruka());
        }
    }//GEN-LAST:event_btnIzbrisiActionPerformed

 

